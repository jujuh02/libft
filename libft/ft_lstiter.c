#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst ->content);
		lst = lst -> next;
	}
}
// iterates through the list, applying f to content of nodes